#ifndef FASTRVC_QUEUE_H
#define FASTRVC_QUEUE_H

#include "osal.h"

typedef struct q_node {
	void *data;
	struct q_node *next;
} q_node;

typedef struct queue {
	bool block_mode;
	unsigned int timeout;
	struct q_node *head;
	struct q_node *tail;
	MUTEX_HANDLE(mutex);
	SEM_HANDLE(sem);
} queue;

queue* queue_create();
void queue_destroy(queue *q);
void queue_timeout(queue *q, unsigned int timeout_ms);
bool queue_block_mode(queue *q, bool block_mode);
void queue_enq(queue *q, void *data);
void queue_enq_head(queue *q, void *data);
void* queue_deq(queue *q);
bool queue_empty(queue *q);

#endif //FASTRVC_QUEUE_H